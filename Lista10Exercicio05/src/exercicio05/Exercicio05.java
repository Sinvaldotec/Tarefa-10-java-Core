package exercicio05;

public class Exercicio05 {

	public static void main(String[] args) {
		// Itera sobre os números de 0 a 20
        for (int i = 0; i <= 20; i++) {
            // Verifica se o número é ímpar
            if (i % 2 != 0) {
                System.out.println(i); // Se for ímpar, imprime-o
            }
        }
    }
}